from turtle import *
t=Turtle()
t.hideturtle()
t.speed(11)
t.right(90)
t.begin_fill()
t.color('yellow')
t.circle(100)
t.end_fill()
t.pu()
t.goto(45,20)
t.pd()
t.begin_fill()
t.color('blue')

t.circle(10,extent=180)
t.fd(10)
t.circle(10,extent=180)
t.fd(10)
t.pu()
t.goto(130,20)
t.pd()

t.circle(10,extent=180)
t.fd(10)
t.circle(10,extent=180)
t.fd(10)

t.end_fill()
t.pu()
t.goto(80,-10)
t.pd()

t.begin_fill()
t.color('black')

t.fd(10)
t.left(90)
t.fd(40)
t.left(90)
t.fd(10)
t.left(90)
t.fd(40)

t.end_fill()

t.pu()
t.goto(50,-40)
t.pd()
t.left(90)


t.begin_fill()
t.color('red')
t.circle(50,extent=180)
t.left(90)
t.fd(100)

t.end_fill()
t.pu()
t.goto(-0,15)
t.pd()
t.right(90)

t.begin_fill()
t.color('black')
t.circle(8,extent=180)
t.fd(10)
t.circle(8,extent=180)
t.fd(10)

t.right(90)
t.pu()
t.goto(200,7)
t.pd()
t.right(90)

t.circle(8,extent=180)
t.fd(10)
t.circle(8,extent=180)
t.fd(10)
t.end_fill()
done()